"""
Посчитать коэффициент линейной регрессии при заработной плате (zp), используя
градиентный спуск (без intercept).
"""
import numpy

ks = numpy.array([401, 574, 874, 919, 459, 739, 653, 902, 746, 832])
zp = numpy.array([35, 45, 190, 200, 40, 70, 54, 150, 120, 110])
alfa = 1e-6
B1 = 0.01
n = 10
for i in range(1500):
    B1 -= alfa * (2 / n) * numpy.sum((B1 * zp - ks) * zp)
    if i % 150 == 0:
        print(f"B1 = {B1}")
# B1 = 5.889820420132673
